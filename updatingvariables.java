package Basics;

public class updatingvariables {
	public static void main(String[] args) {
		float x;
		x = 5;
		System.out.println(x);
		x = 7;
		System.out.println(x);
		x = x + 6;
		System.out.println(x);
		x = x - 20;
		System.out.println(x);
		x = x * -1;
		System.out.println(x);
		x = x + 3;
		System.out.println(x);
		
		
	}

}
